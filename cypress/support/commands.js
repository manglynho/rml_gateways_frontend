Cypress.Commands.add('createGateway', ({ serial, name, ip_v4 }) => {
  cy.request({
    url: 'http://localhost:3001/api/gateways',
    method: 'POST',
    body: { serial, name, ip_v4 }
  })
  cy.visit('http://localhost:3000')
})